	if ((tone.indexOf(chars[cindex+1]) > -1) && (wtone.length > 0)){c_reason = "found another tone"; return true;}
	//check excess vowel
	if ((vowel_need_front.indexOf(formatchar(cindex+1)) > -1) && (v0.replace('อ','').replace('ว','').replace('ย','').length >= possible_min) && (formatyes)){c_reason = "vowel_always_front2"; return true;}
